    # device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    config = TrainConfig()
    dataset_id = config.minari_id
    dataset = minari.load_dataset(dataset_id, download=True)
    env = dataset.recover_environment()

    state_dim = dataset.observation_space.shape[0]
    action_dim = dataset.action_space.shape[0]
    max_action = float(dataset.action_space.high[0])
    if config.normalize_reward:
        raise NotImplementedError
        # modify_reward(dataset, config.env)


    replay_buffer = MinariExperienceReplay(
        dataset_id,
        split_trajs=False,
        batch_size=config.batch_size,
        sampler=SamplerWithoutReplacement(),
        transform=DoubleToFloat(),
    )

    if config.normalize:
        # sample past entire dataset once
        state_mean = torch.mean(replay_buffer.storage['observation'], dim=0).to(config.device)
        state_std = torch.std(replay_buffer.storage['observation'], dim=0).to(config.device)
    else:
        state_mean, state_std = torch.zeros(state_dim), torch.ones(state_dim)

    if config.checkpoints_path is not None:
        print(f"Checkpoints path: {config.checkpoints_path}")
        os.makedirs(config.checkpoints_path, exist_ok=True)
        with open(os.path.join(config.checkpoints_path, "config.yaml"), "w") as f:
            pyrallis.dump(config, f)

    # Set seeds
    seed = config.seed
    set_seed(seed, env)

    q_network = TwinQ(state_dim, action_dim).to(config.device)
    v_network = ValueFunction(state_dim).to(config.device)
    actor = (
        DeterministicPolicy(
            state_dim, action_dim, max_action, dropout=config.actor_dropout
        )
        if config.iql_deterministic
        else GaussianPolicy(
            state_dim, action_dim, max_action, dropout=config.actor_dropout
        )
    ).to(config.device)
    v_optimizer = torch.optim.Adam(v_network.parameters(), lr=config.vf_lr)
    q_optimizer = torch.optim.Adam(q_network.parameters(), lr=config.qf_lr)
    actor_optimizer = torch.optim.Adam(actor.parameters(), lr=config.actor_lr)

    kwargs = {
        "max_action": max_action,
        "actor": actor,
        "actor_optimizer": actor_optimizer,
        "q_network": q_network,
        "q_optimizer": q_optimizer,
        "v_network": v_network,
        "v_optimizer": v_optimizer,
        "discount": config.discount,
        "tau": config.tau,
        "device": config.device,
        # IQL
        "beta": config.beta,
        "iql_tau": config.iql_tau,
        "max_steps": config.max_timesteps,
    }

    print("---------------------------------------")
    print(f"Training IQL, Env: {config.minari_id}, Seed: {seed}")
    print("---------------------------------------")

    # Initialize actor
    trainer = ImplicitQLearning(**kwargs)

    if config.load_model != "":
        policy_file = Path(config.load_model)
        trainer.load_state_dict(torch.load(policy_file))
        actor = trainer.actor

    wandb_init(asdict(config))

    evaluations = []
    for t in range(int(config.max_timesteps)):
        # tensordict keys: ['episode', 'action', 'next', 'observation', 'index']
        batch = replay_buffer.sample()
        batch = batch.to(config.device)
        terminals = torch.logical_or(batch['next']['terminated'], batch['next']['truncated']) # terminal or truncated
        batch["observation"] = normalize_states(
            batch["observation"], state_mean, state_std
        )
        batch['next']['observation'] = normalize_states(
            batch['next']['observation'], state_mean, state_std
        )
        input_batch = [
            batch["observation"].to(torch.float32),
            batch["action"].to(torch.float32),
            batch['next']['reward'].to(torch.float32),
            batch['next']['observation'].to(torch.float32),
            terminals,
            # batch['next']['done'],
        ]
        log_dict = trainer.train(input_batch)
        wandb.log(log_dict, step=trainer.total_it)
        # Evaluate episode
        if (t + 1) % config.eval_freq == 0:
            print(f"Time steps: {t + 1}")
            eval_scores = eval_actor(
                env,
                actor,
                device=config.device,
                n_episodes=config.n_episodes,
                seed=config.seed,
            )
            eval_score = eval_scores.mean()
            # normalized_eval_score = env.get_normalized_score(eval_score) * 100.0
            evaluations.append([t, eval_score])
            # evaluations.append(normalized_eval_score)
            print("---------------------------------------")
            print(
                f"Evaluation over {config.n_episodes} episodes: "
                f"eval_score: {eval_score:.3f}"
            )
            print("---------------------------------------")
            if config.checkpoints_path is not None:
                torch.save(
                    trainer.state_dict(),
                    os.path.join(config.checkpoints_path, f"checkpoint_{t}.pt"),
                )

    x = np.arange(len(trainer.v_loss_hist))
    fig, ax = plt.subplots(1, 1)
    ax.plot(x, trainer.v_loss_hist, label='v_loss')
    ax.plot(x, trainer.q_loss_hist, label='q_loss')
    ax.plot(x, trainer.policy_loss_hist, label='policy_loss')
    ax.set_xlabel('steps')
    ax.set_title('loss_history')
    ax.legend()

    fig2, ax2 = plt.subplots(1, 1)
    eval_history = np.array(evaluations)
    ax2.plot(eval_history[:, 0], eval_history[:, 1], label='eval rewards')
    ax2.set_xlabel('steps')
    ax2.set_title('rewards during evaluation')
    plt.show()

